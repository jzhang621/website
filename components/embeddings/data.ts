export function diffArrays(arr1: number[], arr2: number[]): number[] {
    if (arr1.length !== arr2.length) {
        throw new Error("Arrays must be of equal length");
    }
    return arr1.map((val, idx) => Math.abs(val - arr2[idx]));
}

export function findGlobalMinMax(arrays: number[][]) {
    // Flatten all arrays into a single array
    const combinedArray = arrays.flat();

    // Find the global minimum and maximum using Math.min and Math.max
    const globalMin = Math.min(...combinedArray);
    const globalMax = Math.max(...combinedArray);

    return [globalMin, globalMax];
}
